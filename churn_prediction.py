# 4. FEATURE ENGINEERING
# =============================================================================
def feature_engineering(df):
    print(">> Criando novas features...")

    def tenure_group(t):
        if t <= 12: return "0-1_ano"
        elif t <= 24: return "1-2_anos"
        elif t <= 48: return "2-4_anos"
        else: return "4+_anos"

    df["TenureGroup"] = df["tenure"].apply(tenure_group)

    service_cols = ["PhoneService", "MultipleLines", "OnlineSecurity", "OnlineBackup",
                     "DeviceProtection", "TechSupport", "StreamingTV", "StreamingMovies"]
    df["NumServices"] = df[service_cols].apply(lambda row: sum(v == "Yes" for v in row), axis=1)

    df["AvgMonthlySpend"] = np.where(
        df["tenure"] > 0, df["TotalCharges"] / df["tenure"], df["MonthlyCharges"]
    )

    df["IsNewHighValue"] = (
        (df["tenure"] <= 6) & (df["MonthlyCharges"] > df["MonthlyCharges"].median())
    ).astype(int)

    no_protection_cols = ["OnlineSecurity", "OnlineBackup", "DeviceProtection", "TechSupport"]
    df["HasInternetNoProtection"] = (
        (df["InternetService"] != "No") & (df[no_protection_cols] == "No").all(axis=1)
    ).astype(int)

    df["MonthToMonthElectronicCheck"] = (
        (df["Contract"] == "Month-to-month") & (df["PaymentMethod"] == "Electronic check")
    ).astype(int)

    print("   Features criadas: TenureGroup, NumServices, AvgMonthlySpend, "
          "IsNewHighValue, HasInternetNoProtection, MonthToMonthElectronicCheck")
    return df


