# 5. ENCODING
# =============================================================================
def encode_features(df):
    print(">> Codificando variáveis categóricas...")
    y = (df["Churn"] == "Yes").astype(int)
    X = df.drop(columns=["Churn"])

    obj_cols = X.select_dtypes(include="object").columns
    binary_like = [c for c in obj_cols if X[c].nunique() == 2]
    multi_cat = [c for c in obj_cols if X[c].nunique() > 2]

    print(f"   Label Encoding: {binary_like}")
    print(f"   One-Hot Encoding: {multi_cat}")

    for col in binary_like:
        X[col] = LabelEncoder().fit_transform(X[col])

    X = pd.get_dummies(X, columns=multi_cat, drop_first=True)
    bool_cols = X.select_dtypes(include="bool").columns
    X[bool_cols] = X[bool_cols].astype(int)

    print(f"   Shape final: {X.shape}")
    return X, y


