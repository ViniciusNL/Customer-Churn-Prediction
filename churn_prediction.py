# 9. VISUALIZAÇÕES COMPARATIVAS DOS MODELOS
# =============================================================================
def plot_model_comparisons(results, fitted_models, y_test, metrics_df, feature_names):
    print(">> Gerando visualizações comparativas dos modelos...")

    # Barras comparativas de métricas
    plot_metrics = ["Accuracy", "Precision", "Recall", "F1-Score", "ROC-AUC"]
    plot_df = metrics_df[plot_metrics].loc[MODEL_ORDER]
    fig, ax = plt.subplots(figsize=(12, 6))
    x = np.arange(len(plot_metrics))
    width = 0.2
    for i, model in enumerate(MODEL_ORDER):
        vals = plot_df.loc[model].values
        bars = ax.bar(x + i * width, vals, width, label=model, color=COLOR_MAP[model],
                      edgecolor="white", linewidth=0.8)
        for b, v in zip(bars, vals):
            ax.text(b.get_x() + b.get_width() / 2, v + 0.012, f"{v:.2f}",
                    ha="center", fontsize=8, fontweight="bold", color="#333")
    ax.set_xticks(x + width * 1.5)
    ax.set_xticklabels(plot_metrics, fontsize=11)
    ax.set_ylim(0, 1.05)
    ax.set_ylabel("Score")
    ax.set_title("Comparação de Métricas entre Modelos", fontsize=16, pad=15)
    ax.legend(loc="lower right", ncol=2, frameon=True)
    plt.tight_layout()
    plt.savefig(f"{FIG_DIR}/06_comparacao_metricas.png")
    plt.close()

    # Curvas ROC
    fig, ax = plt.subplots(figsize=(7, 6.5))
    for model in MODEL_ORDER:
        fpr, tpr, _ = roc_curve(y_test, results[model]["y_proba"])
        ax.plot(fpr, tpr, color=COLOR_MAP[model], lw=2.4,
                label=f"{model} (AUC = {results[model]['roc_auc']:.3f})")
    ax.plot([0, 1], [0, 1], linestyle="--", color="#999999", lw=1.3, label="Aleatório (AUC = 0.500)")
    ax.set_xlabel("Taxa de Falsos Positivos (FPR)")
    ax.set_ylabel("Taxa de Verdadeiros Positivos (TPR)")
    ax.set_title("Curvas ROC — Comparação dos Modelos", pad=12)
    ax.legend(loc="lower right", fontsize=9.5)
    plt.tight_layout()
    plt.savefig(f"{FIG_DIR}/07_curvas_roc.png")
    plt.close()

    # Curvas Precision-Recall
    fig, ax = plt.subplots(figsize=(7, 6.5))
    for model in MODEL_ORDER:
        prec, rec, _ = precision_recall_curve(y_test, results[model]["y_proba"])
        ax.plot(rec, prec, color=COLOR_MAP[model], lw=2.4,
                label=f"{model} (AP = {results[model]['avg_precision']:.3f})")
    baseline = y_test.mean()
    ax.axhline(baseline, linestyle="--", color="#999999", lw=1.3,
               label=f"Baseline (taxa de churn = {baseline:.3f})")
    ax.set_xlabel("Recall")
    ax.set_ylabel("Precision")
    ax.set_title("Curvas Precision-Recall — Comparação dos Modelos", pad=12)
    ax.legend(loc="upper right", fontsize=9.5)
    plt.tight_layout()
    plt.savefig(f"{FIG_DIR}/08_curvas_precision_recall.png")
    plt.close()

    # Matrizes de confusão
    fig, axes = plt.subplots(2, 2, figsize=(11, 10))
    for ax, model in zip(axes.flatten(), MODEL_ORDER):
        cm = results[model]["confusion_matrix"]
        cm_pct = cm / cm.sum(axis=1, keepdims=True) * 100
        sns.heatmap(cm, annot=False, cmap=sns.light_palette(COLOR_MAP[model], as_cmap=True),
                    cbar=False, ax=ax, linewidths=2, linecolor="white")
        for r in range(2):
            for c in range(2):
                ax.text(c + 0.5, r + 0.5, f"{cm[r, c]}\n({cm_pct[r, c]:.1f}%)",
                        ha="center", va="center", fontsize=11, fontweight="bold",
                        color="white" if cm_pct[r, c] > 50 else "#333")
        ax.set_xticklabels(["No Churn", "Churn"])
        ax.set_yticklabels(["No Churn", "Churn"], rotation=0)
        ax.set_xlabel("Predito")
        ax.set_ylabel("Real")
        ax.set_title(model, fontsize=13)
    fig.suptitle("Matrizes de Confusão por Modelo", fontsize=16, fontweight="bold", y=1.0)
    plt.tight_layout()
    plt.savefig(f"{FIG_DIR}/09_matrizes_confusao.png")
    plt.close()

    # Feature importance
    tree_models = ["Random Forest", "XGBoost", "LightGBM"]
    fig, axes = plt.subplots(1, 3, figsize=(18, 7))
    for ax, model_name in zip(axes, tree_models):
        model = fitted_models[model_name]
        importances = pd.Series(model.feature_importances_, index=feature_names)
        importances = importances.sort_values(ascending=True).tail(12)
        ax.barh(importances.index, importances.values, color=COLOR_MAP[model_name], edgecolor="white")
        ax.set_title(model_name, fontsize=13)
        ax.set_xlabel("Importância")
    fig.suptitle("Top 12 Features Mais Importantes por Modelo",
                 fontsize=16, fontweight="bold", y=1.03)
    plt.tight_layout()
    plt.savefig(f"{FIG_DIR}/10_feature_importance.png")
    plt.close()

    # Radar comparativo
    radar_metrics = ["Accuracy", "Precision", "Recall", "F1-Score", "ROC-AUC"]
    angles = [n / float(len(radar_metrics)) * 2 * pi for n in range(len(radar_metrics))]
    angles += angles[:1]
    fig, ax = plt.subplots(figsize=(7.5, 7.5), subplot_kw=dict(polar=True))
    for model in MODEL_ORDER:
        vals = metrics_df.loc[model, radar_metrics].tolist()
        vals += vals[:1]
        ax.plot(angles, vals, color=COLOR_MAP[model], linewidth=2.2, label=model)
        ax.fill(angles, vals, color=COLOR_MAP[model], alpha=0.08)
    ax.set_xticks(angles[:-1])
    ax.set_xticklabels(radar_metrics, fontsize=11)
    ax.set_ylim(0.4, 1.0)
    ax.set_title("Radar Comparativo de Métricas", pad=25, fontsize=15)
    ax.legend(loc="upper right", bbox_to_anchor=(1.35, 1.1), fontsize=9.5)
    plt.tight_layout()
    plt.savefig(f"{FIG_DIR}/11_radar_comparativo.png")
    plt.close()

    print("   6 gráficos comparativos salvos em", FIG_DIR)


