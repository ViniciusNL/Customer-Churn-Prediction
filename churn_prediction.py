# 2. TRATAMENTO DE VALORES AUSENTES
# =============================================================================
def clean_missing(df):
    print(">> Tratando valores ausentes...")
    # TotalCharges vem como texto e tem 11 registros em branco (clientes com
    # tenure = 0, isto é, recém-assinados e ainda sem um ciclo de cobrança).
    df["TotalCharges"] = df["TotalCharges"].replace(" ", np.nan).astype(float)
    n_missing = df["TotalCharges"].isnull().sum()
    print(f"   {n_missing} valores ausentes encontrados em TotalCharges "
          f"(clientes com tenure=0)")
    # Imputação coerente com o negócio: usar MonthlyCharges como proxy,
    # já que o cliente ainda não fechou um ciclo completo.
    df.loc[df["TotalCharges"].isnull(), "TotalCharges"] = df.loc[
        df["TotalCharges"].isnull(), "MonthlyCharges"
    ]
    assert df["TotalCharges"].isnull().sum() == 0
    df = df.drop(columns=["customerID"])
    return df


